    private void registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrarActionPerformed
        // TODO add your handling code here:
        com.mysql.jdbc.Connection con = null; // Se establece conexion
try{
con = getConection();
ps = con.prepareStatement("INSERT INTO cliente (id_cliente,nombre, apellido, direccion, telefono,email) VALUES(?,?,?,?,?,?)");
//Gestion de los datos del Usuario
ps.setString(1, idcliente.getText());
ps.setString(2, nombre.getText());
ps.setString(3, apellido.getText());
ps.setString(4, direccion.getText());
ps.setString(5, tel.getText());
ps.setString(6, email.getText());

//Ejecucion del SQL, para crear y modigicar registros
int res = ps.executeUpdate();//
if(res > 0){
JOptionPane.showMessageDialog(null, "cliente Guardada exitosamente");
} else {
JOptionPane.showMessageDialog(null, "Error al Guardar el cliente");
}
con.close();

} catch(Exception e){
System.err.println(e);
}
    }//GEN-LAST:event_registrarActionPerformed

